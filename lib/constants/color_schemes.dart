import 'package:flutter/material.dart';

/// [ColorSchemes] is a class that holds all the color schemes used in the app.
class ColorSchemes {
  /// [primary] is the primary color of the app.
  static const Color primary = Color(0xFFD7CFFF);

  /// [secondary] is the secondary color of the app.
  static const Color secondary = Color(0xFF0BAC7D);

  /// [backgroundPrimary] is the primary background color of the app.
  static const Color backgroundPrimary = Color(0xFFFFFFFF);

  /// [textPrimary] is the primary text color of the app.
  static const Color textPrimary = Color(0xFF000000);

  /// [subtle] is the subtle color of the app.
  static const Color subtle = Color(0xFFD9D9D9);

  /// [highlight] is the highlight color of the app.
  static const Color highlight = Color(0xFF757575);
}
